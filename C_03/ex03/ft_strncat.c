#include <unistd.h>

char	*ft_strncat(char *dest, char *src, unsigned int nb)
{
	unsigned int	i;
	unsigned int	j;

	i = 0;
	j = 0;
	while (dest[i])
		i++;
	while (src[j] && j < nb)
	{
		dest[i] = src[j];
		i++;
		j++;
	}
	dest[i] = '\0';
	return (dest);
}

#include <stdio.h>

int main(void)
{
	char dest[50] = "Hello, ";
	char src[] = "world!";
	unsigned int nb = 3;

	printf("Before: %s\n", dest);
	ft_strncat(dest, src, nb);
	printf("After: %s\n", dest);

	return 0;
}