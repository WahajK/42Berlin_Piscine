int	parse_input(char *input, int clues[16])
{
	int	i;
	int	j;

	i = 0;
	j = 0;
	while (input[j] != '\0')
	{
		if (j % 2 != 0 && input[j] != ' ')
			return (0);
		if (input[j] >= '1' && input[j] <= '4')
			clues[i++] = input[j] - '0';
		j++;
	}
	return (i == 16);
}
